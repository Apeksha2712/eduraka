package edureka;

public class IfElse {
public static void main(String[] args) {
	int a=10;
		if(a<20) {
			System.out.println("a is less than 20 ");//in if block always true statement write
		System.out.println("Hello");
		}
		else { 
			System.out.println("a is greater then 20");
		System.out.println("World");
		}
}
}
