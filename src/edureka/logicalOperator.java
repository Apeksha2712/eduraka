package edureka;

public class logicalOperator {
public static void main(String[] args) {
	int a=10;
	int b=20;
	int c=5;
	System.out.println(a<b&&a>c);   //Using logical AND operator
	                                //in logical AND both operand should be true 
	System.out.println(a<b||a>c);//Using logical OR Operator
	                              //in logical OR ant one operand should be true
	
}
}
